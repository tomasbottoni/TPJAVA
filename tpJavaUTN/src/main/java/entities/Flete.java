package entities;

public class Flete {
	private int id_flete;
	private String nombre;
	
	public int getId_flete() {
		return id_flete;
	}
	public void setId_flete(int id_flete) {
		this.id_flete = id_flete;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	
	
}
